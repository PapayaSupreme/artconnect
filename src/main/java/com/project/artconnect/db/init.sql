-- ===== Views =====
-- 1) Security view: expose artist profile without private direct contacts.
CREATE VIEW vw_public_artist_profiles AS
SELECT
    a.id,
    a.name,
    a.bio,
    a.birth_year,
    a.city,
    a.website,
    a.social_media,
    a.is_active
FROM artists a;

-- 2) Simplification view: one-row workshop capacity snapshot.
CREATE VIEW vw_workshop_capacity AS
SELECT
    w.id AS workshop_id,
    w.title,
    w.workshop_date,
    w.level,
    w.max_participants,
    COUNT(*) FILTER (WHERE b.payment_status <> 'CANCELLED')::INT AS confirmed_participants,
    CASE
        WHEN w.max_participants IS NULL THEN NULL
        ELSE (w.max_participants - COUNT(*) FILTER (WHERE b.payment_status <> 'CANCELLED'))::INT
    END AS available_seats
FROM workshops w
LEFT JOIN bookings b ON b.workshop_id = w.id
GROUP BY w.id, w.title, w.workshop_date, w.level, w.max_participants;

-- 3) Analytics view: aggregate member engagement for quick community reporting.
CREATE VIEW vw_member_activity_summary AS
SELECT
    m.id AS member_id,
    m.name,
    m.email,
    m.membership_type,
    COUNT(DISTINCT (b.workshop_id, b.member_id)) AS bookings_count,
    COUNT(DISTINCT r.artwork_id) AS reviews_count,
    MAX(b.booking_date) AS last_booking_at,
    MAX(r.review_date) AS last_review_on
FROM community_members m
LEFT JOIN bookings b ON b.member_id = m.id
LEFT JOIN reviews r ON r.reviewer_member_id = m.id
GROUP BY m.id, m.name, m.email, m.membership_type;

