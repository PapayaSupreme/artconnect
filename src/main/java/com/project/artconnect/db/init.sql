-- ===== Stored programs (functions/procedure) =====
-- Returns active (non-cancelled) participant count for one workshop.
CREATE OR REPLACE FUNCTION fn_workshop_participant_count(p_workshop_id BIGINT)
RETURNS INT
LANGUAGE SQL
AS $$
    SELECT COUNT(*)::INT
    FROM bookings
    WHERE workshop_id = p_workshop_id
      AND payment_status <> 'CANCELLED';
$$;

-- Returns available seats; NULL means unlimited workshop capacity.
CREATE OR REPLACE FUNCTION fn_workshop_available_seats(p_workshop_id BIGINT)
RETURNS INT
LANGUAGE plpgsql
AS $$
DECLARE
    v_max INT;
BEGIN
    SELECT max_participants INTO v_max
    FROM workshops
    WHERE id = p_workshop_id;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Workshop % does not exist.', p_workshop_id;
    END IF;

    IF v_max IS NULL THEN
        RETURN NULL;
    END IF;

    RETURN v_max - fn_workshop_participant_count(p_workshop_id);
END;
$$;

-- Returns average rating received by an artist across their artworks.
CREATE OR REPLACE FUNCTION fn_artist_average_rating(p_artist_id BIGINT)
RETURNS DECIMAL(4, 2)
LANGUAGE SQL
AS $$
    SELECT ROUND(AVG(r.rating)::NUMERIC, 2)::DECIMAL(4, 2)
    FROM reviews r
    JOIN artworks a ON a.id = r.artwork_id
    WHERE a.artist_id = p_artist_id;
$$;

-- Atomic operation: register one member to multiple workshops in one call.
CREATE OR REPLACE PROCEDURE sp_register_member_for_workshops(
    p_member_id BIGINT,
    p_workshop_ids BIGINT[]
)
LANGUAGE plpgsql
AS $$
DECLARE
    v_workshop_id BIGINT;
BEGIN
    IF p_workshop_ids IS NULL OR ARRAY_LENGTH(p_workshop_ids, 1) IS NULL THEN
        RAISE EXCEPTION 'workshop_ids cannot be empty.';
    END IF;

    PERFORM 1 FROM community_members WHERE id = p_member_id;
    IF NOT FOUND THEN
        RAISE EXCEPTION 'Member % not found.', p_member_id;
    END IF;

    FOREACH v_workshop_id IN ARRAY p_workshop_ids LOOP
        INSERT INTO bookings (workshop_id, member_id, booking_date, payment_status)
        VALUES (v_workshop_id, p_member_id, CURRENT_TIMESTAMP, 'PENDING');
    END LOOP;
END;
$$;

