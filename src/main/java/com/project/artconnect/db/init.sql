-- ===== Trigger support tables and functions =====
CREATE TABLE workshop_audit (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    workshop_id BIGINT,
    operation VARCHAR(10) NOT NULL,
    changed_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    changed_by TEXT NOT NULL DEFAULT CURRENT_USER,
    old_data JSONB,
    new_data JSONB
);

-- Normalizes email + membership type to keep input canonical for lookups.
CREATE OR REPLACE FUNCTION fn_normalize_member_data()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    NEW.email := LOWER(TRIM(NEW.email));

    IF NEW.membership_type IS NOT NULL THEN
        NEW.membership_type := LOWER(TRIM(NEW.membership_type));
    END IF;

    RETURN NEW;
END;
$$;

-- Ensures booking date consistency and protects workshop capacity.
CREATE OR REPLACE FUNCTION fn_validate_booking_capacity_and_date()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_workshop_date TIMESTAMP;
    v_max_participants INT;
    v_active_bookings INT;
BEGIN
    SELECT workshop_date, max_participants
    INTO v_workshop_date, v_max_participants
    FROM workshops
    WHERE id = NEW.workshop_id
    FOR UPDATE;

    IF NOT FOUND THEN
        RAISE EXCEPTION 'Workshop % not found for booking.', NEW.workshop_id;
    END IF;

    IF NEW.booking_date IS NULL THEN
        NEW.booking_date := CURRENT_TIMESTAMP;
    END IF;

    IF NEW.booking_date > v_workshop_date THEN
        RAISE EXCEPTION 'Booking date (%) cannot be after workshop date (%).', NEW.booking_date, v_workshop_date;
    END IF;

    IF COALESCE(NEW.payment_status, 'PENDING') <> 'CANCELLED' AND v_max_participants IS NOT NULL THEN
        SELECT COUNT(*)
        INTO v_active_bookings
        FROM bookings b
        WHERE b.workshop_id = NEW.workshop_id
          AND b.payment_status <> 'CANCELLED'
          AND (
              TG_OP = 'INSERT'
              OR (b.workshop_id, b.member_id) <> (OLD.workshop_id, OLD.member_id)
          );

        IF v_active_bookings >= v_max_participants THEN
            RAISE EXCEPTION 'Workshop % is full (% seats).', NEW.workshop_id, v_max_participants;
        END IF;
    END IF;

    RETURN NEW;
END;
$$;

-- Keeps artwork status aligned with exhibition participation.
CREATE OR REPLACE FUNCTION fn_sync_artwork_exhibited_status()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
DECLARE
    v_artwork_id BIGINT;
    v_links INT;
BEGIN
    v_artwork_id := COALESCE(NEW.artwork_id, OLD.artwork_id);

    IF TG_OP = 'INSERT' THEN
        UPDATE artworks
        SET status = 'EXHIBITED'
        WHERE id = NEW.artwork_id
          AND status <> 'SOLD';
        RETURN NEW;
    END IF;

    SELECT COUNT(*) INTO v_links
    FROM exhibition_artworks
    WHERE artwork_id = v_artwork_id;

    IF v_links = 0 THEN
        UPDATE artworks
        SET status = 'FOR_SALE'
        WHERE id = v_artwork_id
          AND status = 'EXHIBITED';
    END IF;

    RETURN COALESCE(NEW, OLD);
END;
$$;

-- Audits workshop lifecycle changes for accountability.
CREATE OR REPLACE FUNCTION fn_workshops_audit()
RETURNS TRIGGER
LANGUAGE plpgsql
AS $$
BEGIN
    INSERT INTO workshop_audit (workshop_id, operation, old_data, new_data)
    VALUES (
        COALESCE(NEW.id, OLD.id),
        TG_OP,
        CASE WHEN TG_OP IN ('UPDATE', 'DELETE') THEN TO_JSONB(OLD) ELSE NULL END,
        CASE WHEN TG_OP IN ('INSERT', 'UPDATE') THEN TO_JSONB(NEW) ELSE NULL END
    );

    RETURN COALESCE(NEW, OLD);
END;
$$;

